
/* 
Faça as seguintes operações usando o computador:

Somar 3 com 4
Multiplicar 3 com 5 e dividir o resultado por 2
Subtrair 5 de 4 e multiplicar o resultado por -1
Determinar o resto da divisão de 234 por 5


*/
// 1. Somar 3 com 4
var soma = 3 + 4
console.log("1. A soma de 3 com 4 é: " + soma)

// 2. Multiplicar 3 com 5 e dividir o resultado por 2
var multiplicacaoDivisao = (3 * 5) / 2
console.log("2. O resultado de (3 * 5) / 2 é: " + multiplicacaoDivisao)

// 3. Subtrair 5 de 4 e multiplicar o resultado por -1
var subtracaoMultiplicacao = (4 - 5) * -1
console.log("3. O resultado de (4 - 5) * -1 é: " + subtracaoMultiplicacao)

// 4. Determinar o resto da divisão de 234 por 5
var restoDivisao = 234 % 5;
console.log("4. O resto da divisão de 234 por 5 é: " + restoDivisao)
