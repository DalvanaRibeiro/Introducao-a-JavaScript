/*
Algoritmo para trocar uma lâmpada:
Passo 1: Verificar a necessidade
Verifique se a lâmpada realmente está queimada ou se o problema pode ser outro (como um interruptor desligado).

Passo 2: Segurança primeiro
Desligue o interruptor que controla a lâmpada.
Se possível, desligue a energia elétrica do local para garantir segurança total.

Passo 3: Acesso à lâmpada
Pegue uma escada (se necessário e obviamente é, considerando a altura da professora) e posicione-a embaixo da lâmpada.
Certifique-se de que a escada está firme e segura.

Passo 4: Remover a lâmpada queimada
Gire a lâmpada antiga no sentido anti-horário (esquerda) até que ela se solte do soquete.
Segure a lâmpada com cuidado para não deixá-la cair.

Passo 5: Verificar a lâmpada nova
Verifique se a nova lâmpada tem a mesma potência e compatibilidade que a lâmpada antiga.

Passo 6: Instalar a nova lâmpada
Insira a nova lâmpada no soquete.
Gire-a no sentido horário (direita) até que ela esteja firmemente instalada, mas não aperte demais.

Passo 7: Testar
Ligue o interruptor novamente e veja se a lâmpada acende.
Passo 8: Limpeza e organização

Guarde a lâmpada velha em um local apropriado para reciclagem.
Limpe o ambiente e guarde a escada.


*/