// Criação das variáveis
var primeiroNome = "Maria";        // Substitua pelo seu nome (Variável do tipo String, sempre usamos " ")
var sobrenome = "De Jesus";       // Substitua pelo seu sobrenome (Variável do tipo String, sempre usamos " ")
var idade = 56;                 // Substitua pela sua idade (Variável do tipo number)
var estudante = true;           // Substitua por false se você não for estudante (Variável do tipo boolean)

// Impressão no console
console.log("Nome: " + primeiroNome); 
console.log("Sobrenome: " + sobrenome);
console.log("Idade: " + idade);
console.log("É estudante: " + (estudante ? "Sim" : "Não"));
